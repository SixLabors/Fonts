namespace SixLabors.Fonts;

/// <summary>
/// Identifies the visual weight of a font on the OpenType and CSS weight scale.
/// </summary>
public enum FontWeight
{
    /// <summary>
    /// Thin weight (100).
    /// </summary>
    Thin = 100,

    /// <summary>
    /// Extra-light weight (200).
    /// </summary>
    ExtraLight = 200,

    /// <summary>
    /// Light weight (300).
    /// </summary>
    Light = 300,

    /// <summary>
    /// Normal weight (400).
    /// </summary>
    Normal = 400,

    /// <summary>
    /// Medium weight (500).
    /// </summary>
    Medium = 500,

    /// <summary>
    /// Semi-bold weight (600).
    /// </summary>
    SemiBold = 600,

    /// <summary>
    /// Bold weight (700).
    /// </summary>
    Bold = 700,

    /// <summary>
    /// Extra-bold weight (800).
    /// </summary>
    ExtraBold = 800,

    /// <summary>
    /// Black weight (900).
    /// </summary>
    Black = 900
}
